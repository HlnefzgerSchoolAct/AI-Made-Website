class ShortcutManager {
    constructor() { this.shortcuts = {}; this.init(); }
    init() { this.registerShortcuts(); this.bindEvents(); }
    registerShortcuts() {
        this.shortcuts = {
            'f': () => document.getElementById('focus-mode-btn')?.click(),
            's': () => document.getElementById('sounds-btn')?.click(),
            't': () => document.getElementById('add-task-btn')?.click(),
            'n': () => document.getElementById('add-note-btn')?.click()
        };
    }
    bindEvents() {
        document.addEventListener('keydown', (e) => {
            if (e.ctrlKey || e.metaKey) return;
            if (e.target.tagName === 'INPUT' || e.target.tagName === 'TEXTAREA') return;
            const handler = this.shortcuts[e.key.toLowerCase()];
            if (handler) { e.preventDefault(); handler(); }
        });
    }
}
window.shortcutManager = new ShortcutManager();

